/**
 * Apply translations to all elements with data-i18n attribute
 * @param {string} lang - 'es' or 'en'
 */
export function applyLanguage(lang) {
    const t = translations[lang];
    if (!t) return;

    document.documentElement.lang = lang;
    document.title = t.pageTitle;

    // Update meta description
    const metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc) metaDesc.setAttribute('content', t.pageDesc);

    // Apply to all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        if (t[key] !== undefined) {
            // Check if element has child elements we need to preserve (like <span class="prompt">)
            const prompt = el.querySelector('.prompt');
            if (prompt) {
                // Rebuild: keep the prompt span, replace the text
                el.innerHTML = '';
                el.appendChild(prompt);
                el.append(' ' + t[key]);
            } else if (el.querySelector('svg')) {
                // Has SVG icon — only replace the text node
                const svg = el.querySelector('svg');
                el.textContent = '';
                el.appendChild(svg);
                el.append('\n                    ' + t[key]);
            } else {
                el.textContent = t[key];
            }
        }
    });

    return t;
}
